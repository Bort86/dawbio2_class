package model;

import java.util.Date;

/**
 *
 * @author Pablo Rodriguez
 */
public class Web {
    private String code;
    private Date date;
    private String title;
    private String url;

    public Web() {
    }

    public Web(String code, Date date, String title, String url) {
        this.code = code;
        this.date = date;
        this.title = title;
        this.url = url;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
    
    
}
